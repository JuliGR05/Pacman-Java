    public int contarPellets() {
        int contador = 0;
        for (int i = 0; i < mapa.length; i++) {
            for (int j = 0; j < mapa[0].length; j++) {
                if (mapa[i][j] == PELLET) {
                    contador++;
                }
            }
        }
        return contador;
    }
